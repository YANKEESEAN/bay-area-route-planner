def geocode_address(address, api_key):
    """
    使用高德地图地理编码API将地址转换为经纬度坐标
    返回格式：'经度,纬度'
    """
    url = "https://restapi.amap.com/v3/geocode/geo"
    params = {
        'key': api_key,
        'address': address,
        'output': 'JSON'
    }

    try:
        response = requests.get(url, params=params, timeout=10)
        data = response.json()

        if data['status'] == '1' and data['geocodes']:
            location = data['geocodes'][0]['location']
            print(f"成功获取坐标：{address} -> {location}")
            return location
        else:
            print(f"无法获取地址坐标：{address}，错误信息：{data.get('info', 'Unknown error')}")
            return None
    except Exception as e:
        print(f"请求异常：{address}，异常信息：{e}")
        return None


def batch_geocode(locations_dict, api_key, batch_size=10, delay=0.2):
    """
    批量地理编码函数
    """
    results = {}
    failed_addresses = []

    print(f"开始处理 {len(locations_dict)} 个景点的地理编码...")

    # 将字典转换为列表以便分批处理
    items = list(locations_dict.items())

    for i in range(0, len(items), batch_size):
        batch = items[i:i + batch_size]

        for name, address in batch:
            location = geocode_address(address, api_key)

            if location:
                results[name] = location
            else:
                results[name] = None
                failed_addresses.append(name)

            # 添加延迟，避免触发API限制
            time.sleep(delay)

        print(f"已完成 {min(i + batch_size, len(items))}/{len(items)} 个景点")

    return results, failed_addresses


def save_results(results, failed_addresses, filename='景点坐标结果.csv'):
    """
    保存结果到CSV文件
    """
    # 准备数据
    data = []
    for name, location in results.items():
        if location:
            lon, lat = location.split(',')
            data.append({
                '景点名称': name,
                '经度': lon,
                '纬度': lat,
                '坐标': location,
                '状态': '成功'
            })
        else:
            data.append({
                '景点名称': name,
                '经度': '',
                '纬度': '',
                '坐标': '',
                '状态': '失败'
            })

    # 创建DataFrame并保存
    df = pd.DataFrame(data)
    df.to_csv(filename, index=False, encoding='utf-8-sig')

    print(f"\n结果已保存到：{filename}")
    print(f"成功获取坐标：{len(results) - len(failed_addresses)} 个")
    print(f"获取失败：{len(failed_addresses)} 个")

    if failed_addresses:
        print("失败的景点：")
        for name in failed_addresses:
            print(f"  - {name}")


